import turtle                     #https://michael0x2a.com/blog/turtle-examples 
chcolor=turtle.Turtle()
chcolor.pencolor("blue")
for i in range(50):
    chcolor.forward(50)
    chcolor.left(123)
chcolor.pencolor("green")
for i in range(50):
    chcolor.forward(100)
    chcolor.left(128)
turtle.done()
    
